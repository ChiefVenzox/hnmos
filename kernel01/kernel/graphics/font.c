static const u8 *hnm_font_glyph(char character)
{
    if (character >= 'A' && character <= 'Z') {
        return hnm_font_upper[character - 'A'];
    }

    if (character >= 'a' && character <= 'z') {
        return hnm_font_lower[character - 'a'];
    }

    if (character >= '0' && character <= '9') {
        return hnm_font_digits[character - '0'];
    }

    if (character == ' ') {
        return hnm_font_space;
    }

    if (character == ':') {
        return hnm_font_colon;
    }

    if (character == '.') {
        return hnm_font_dot;
    }

    if (character == '-') {
        return hnm_font_dash;
    }

    if (character == '/') {
        return hnm_font_slash;
    }

    if (character == '>') {
        return hnm_font_greater;
    }

    return hnm_font_fallback;
}

void hnm_font_draw_char(u32 x, u32 y, char character, hnm_color fg, hnm_color bg)
{
    const u8 *glyph = hnm_font_glyph(character);

    for (u32 row = 0; row < HNM_FONT_HEIGHT; row++) {
        for (u32 col = 0; col < HNM_FONT_WIDTH; col++) {
            hnm_color color = (glyph[row] & (1 << (HNM_FONT_WIDTH - 1 - col))) != 0 ? fg : bg;

            hnm_framebuffer_put_pixel(x + col, y + row, color);
        }
    }
}

void hnm_font_draw_string(u32 x, u32 y, const char *text, hnm_color fg, hnm_color bg)
{
    for (u32 i = 0; text[i] != '\0'; i++) {
        hnm_font_draw_char(x + (i * HNM_FONT_WIDTH), y, text[i], fg, bg);
    }
}

u32 hnm_font_string_width(const char *text)
{
    u32 length = 0;

    while (text[length] != '\0') {
        length++;
    }

    return length * HNM_FONT_WIDTH;
}
